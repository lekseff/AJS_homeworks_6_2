import validateNumber from '../app';

test.each([
  ['8 (927) 000-00-00', '+79270000000'],
  ['+7 960 000 00 00', '+79600000000'],
  ['+86 000 000 0000', '+860000000000'],
  ['+7-960-000-00-00', '+79600000000'],
  ['+79600000000', '+79600000000'],
  ['89600000000', '+79600000000'],
  ['+5 (960)000-00-00', '+59600000000'],
])('Checking phone numbers', (received, expected) => {
  expect(validateNumber(received)).toBe(expected);
});
